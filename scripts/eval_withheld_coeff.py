"""Withheld-coefficient evaluation (P0-2a).

Train + label on Config A; evaluate on faults generated by Config B (a
different generator). The gap between same-config and withheld-config macro-F1
quantifies generator-overfit — the most honest synthetic number obtainable
without real faults.

Run:
    python -m scripts.eval_withheld_coeff

Output:
    results/withheld_coeff_results.json
        { same_config_macro_f1, withheld_config_macro_f1, gap, ... }

HONESTY RULE (do not violate): Config B is not tuned to shrink the gap. A large
gap is the correct, informative outcome — report it as-is.
"""

from __future__ import annotations

import json
import logging
import sys
from pathlib import Path
from typing import Optional

import pandas as pd

logging.basicConfig(level=logging.INFO, format="%(message)s")
log = logging.getLogger(__name__)

_REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(_REPO))

from src.features.dataset_builder import build_dataset
from src.injection.eval_configs import CONFIG_A, CONFIG_B, InjectionConfig
from src.models import xgb_classifier
from src.models.classifier import session_split


def _build_for(config: InjectionConfig, output_name: str) -> pd.DataFrame:
    return build_dataset(
        magnitudes=config.magnitudes,
        onset_fraction=config.onset_fraction,
        ramp_fraction=config.ramp_fraction,
        noise_std=config.noise_std,
        output_name=output_name,
    )


def compute_withheld_gap(
    ds_a: Optional[pd.DataFrame] = None,
    ds_b: Optional[pd.DataFrame] = None,
    *,
    n_estimators: int = 300,
    random_seed: int = 42,
) -> dict:
    """Train on Config-A data, score on both A-test and B-withheld; return the gap.

    Parameters
    ----------
    ds_a, ds_b : pd.DataFrame or None
        Pre-built datasets (used by tests). When None, they are built from
        carOBD via ``build_dataset`` with the respective config.
    n_estimators, random_seed : forwarded to the classifier.

    Returns
    -------
    dict with same_config_macro_f1, withheld_config_macro_f1, gap, and context.
    """
    if ds_a is None:
        log.info("Building Config-A (training) dataset …")
        ds_a = _build_for(CONFIG_A, "dataset_configA")
    if ds_b is None:
        log.info("Building Config-B (withheld) dataset …")
        ds_b = _build_for(CONFIG_B, "dataset_configB")

    # Train on Config A's training split.
    train_a, test_a = session_split(ds_a)
    log.info("Training classifier on Config-A (%d train rows) …", len(train_a))
    clf, norm = xgb_classifier.train(train_a, n_estimators=n_estimators, random_seed=random_seed)

    # Same-config: score on Config A's held-out sessions.
    res_a = xgb_classifier.evaluate(clf, norm, test_a)
    same_f1 = float(res_a["macro_f1"])

    # Withheld-config: score the SAME model on Config B's held-out sessions
    # (same session IDs, faults regenerated by a different generator).
    _, test_b = session_split(ds_b)
    res_b = xgb_classifier.evaluate(clf, norm, test_b)
    withheld_f1 = float(res_b["macro_f1"])

    gap = same_f1 - withheld_f1
    return {
        "same_config_macro_f1": same_f1,
        "withheld_config_macro_f1": withheld_f1,
        "gap": gap,
        "config_a": {
            "name": CONFIG_A.name,
            "magnitudes": CONFIG_A.magnitudes,
            "onset_fraction": CONFIG_A.onset_fraction,
            "ramp_fraction": CONFIG_A.ramp_fraction,
            "noise_std": CONFIG_A.noise_std,
        },
        "config_b": {
            "name": CONFIG_B.name,
            "magnitudes": CONFIG_B.magnitudes,
            "onset_fraction": CONFIG_B.onset_fraction,
            "ramp_fraction": CONFIG_B.ramp_fraction,
            "noise_std": CONFIG_B.noise_std,
        },
        "n_test_same": int(len(test_a)),
        "n_test_withheld": int(len(test_b)),
        "interpretation": (
            "gap = same_config − withheld_config macro-F1. A LARGE gap means "
            "the headline synthetic F1 is substantially generator-overfit and "
            "would not transfer to faults the model did not author. This is an "
            "honest, expected outcome — Config B is not tuned to shrink it."
        ),
    }


def main() -> int:
    results = compute_withheld_gap()

    out_path = _REPO / "results" / "withheld_coeff_results.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        json.dump(results, f, indent=2)

    log.info("")
    log.info("Same-config   macro-F1: %.4f", results["same_config_macro_f1"])
    log.info("Withheld-config macro-F1: %.4f", results["withheld_config_macro_f1"])
    log.info("Gap: %.4f  (large = generator-overfit; honest outcome)", results["gap"])
    log.info("Written: %s", out_path)
    return 0


if __name__ == "__main__":
    sys.exit(main())
