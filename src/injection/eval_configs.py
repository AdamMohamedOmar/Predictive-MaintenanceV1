"""Two injector configurations for the withheld-coefficient evaluation (P0-2).

The problem this addresses
--------------------------
Every fault in the training set comes from one deterministic generator, and
the severity target was (historically) the algebraic inverse of that
generator's coefficients.  Session-splitting does not break this: a held-out
session's faults are produced by the *same* injector, so the held-out F1
measures generator-recovery, not fault detection.

The withheld-coefficient protocol
----------------------------------
Train and label on **Config A**.  Evaluate on faults generated by **Config B**
— a different generator with different magnitudes, onsets, ramp shapes, and
noise.  The gap between same-config F1 and withheld-config F1 quantifies how
much of the headline number is generator-overfit.  This is the standard
train-on-one-distribution / test-on-another protocol for synthetic fault data
(cf. "A Review of Fault Diagnosis Methods", PMC12846000).

Honesty rule: Config B is NOT tuned to make the gap small.  A large gap is the
correct, informative outcome — it tells the thesis exactly how far the
synthetic number is from a real-fault number.
"""

from __future__ import annotations

from dataclasses import dataclass

from src.injection.fault_injector import _DEFAULT_MAGNITUDE


@dataclass(frozen=True)
class InjectionConfig:
    """A complete, reproducible fault-generator configuration."""

    name: str
    magnitudes: dict[str, float]   # per-fault magnitude
    onset_fraction: float          # fraction of session before fault onset
    ramp_fraction: float           # fraction of session used for the ramp
    noise_std: float               # Gaussian noise multiplier


# Config A — the TRAINING generator (the deployed defaults).
CONFIG_A = InjectionConfig(
    name="train",
    magnitudes=dict(_DEFAULT_MAGNITUDE),
    onset_fraction=0.40,
    ramp_fraction=0.15,
    noise_std=0.3,
)

# Config B — the WITHHELD evaluation generator.  Deliberately different on
# every axis: magnitudes shifted per fault (some milder, some more severe),
# later onset, longer ramp, more noise.  NEVER used to train.  Not tuned to
# close the gap — see the honesty rule in the module docstring.
CONFIG_B = InjectionConfig(
    name="withheld_eval",
    magnitudes={
        "air_system": 9.0,                 # 13 → 9  (smaller leak)
        "fuel_system": 24.0,               # 18 → 24 (more severe lean)
        "coolant_temp_sensor": 38.0,       # 42 → 38 (colder stuck value)
        "throttle_position_sensor": 1.5,   # 1.35 → 1.5 (larger TPS drift)
    },
    onset_fraction=0.55,
    ramp_fraction=0.25,
    noise_std=0.5,
)
